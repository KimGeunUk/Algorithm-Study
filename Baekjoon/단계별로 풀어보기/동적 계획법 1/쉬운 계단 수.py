N = int(input())

DP = [[1] * 9 for _ in range(N)]

for i in range(1, N):
    for j in range(9):
        if j == 0:
            if i < 2:
                DP[i][j] = DP[i-1][j+1] + 1
            else:
                DP[i][j] = DP[i-1][j+1] + DP[i-2][j]
        elif j == 8:
            DP[i][j] = DP[i-1][j-1]
        else:
            DP[i][j] = DP[i-1][j-1] + DP[i-1][j+1]
    
print(sum(DP[N-1]) % 1000000000)
    
